#include "checker.h"

int		print_action(char *str)
{
	int	i;
	int	j;

	j = ft_strlen(str);
	i = 0;
	if (j > 0)
	{
		while (i < j)
		{
			write(1, &(str[i]), 1);
			i++;
		}
		write(1, "\n", 2);
	}
	return (0);
}
